import mongoose from "mongoose";

const Authschema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const AuthModel = mongoose.models.User || mongoose.model("User", Authschema);
export default AuthModel;
